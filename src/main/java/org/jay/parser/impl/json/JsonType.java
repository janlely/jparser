package org.jay.parser.impl.json;

/**
 * type of json value
 */
public enum JsonType {
    /**
     * string
     */
    STRING,
    /**
     * object
     */
    OBJECT,
    /**
     * boolean
     */
    BOOL,
    /**
     * array
     */
    ARRAY,
    /**
     * number
     */
    NUMBER,
    /**
     * null
     */
    NULL;
}
